import * as React from "react";

import { cn } from "@/lib/utils";

/**
 * Card 组件（shadcn/ui 标准实现）
 *
 * SPEC 3.1：圆角 6-8px（--radius: 0.5rem = 8px），8px 网格系统。
 */
